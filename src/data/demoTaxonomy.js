// Helper function to get words for a specific domain
export function getWordsForDomain(domainId) {
  return DEMO_VOCABULARY.filter(word => word.domains.includes(domainId));
}

// Helper function to get domain by ID
export function getDomainById(domainId) {
  return DEMO_TAXONOMY.domains.find(d => d.id === domainId);
}

// Helper function to get word by ID
export function getWordById(wordId) {
  return DEMO_VOCABULARY.find(w => w.id === wordId);
}

// Helper function to get connected words
export function getConnectedWords(wordId) {
  const word = getWordById(wordId);
  if (!word || !word.connections) return [];

  return word.connections
    .map(connId => DEMO_VOCABULARY.find(w => w.word === connId || w.id === connId))
    .filter(Boolean);
}
